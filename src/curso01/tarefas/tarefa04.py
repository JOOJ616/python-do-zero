n = input("Digite a palavra a ser consultada: ")
print(type(n))
print(n.isalnum())  # numero?
print(n.isalpha())  # letra?
print(n.isdecimal())  # decimal?
print(n.isdigit())  # capitalizada
print(n.islower())  # letra minuscula
print(n.isupper())  # letra maiscula
print(n.isprintable())  # se pode ser printado
print(n.isspace())  # se tem espa√ßo
